package loesungen.aufgabe_3;

public class aufgabe {

    public static void main(String[] args){

        /*
        Nr. 1:
        Erstelle eine Variable names x und weise ihr den Wert 15 zu. Dann machst du eine Abfrage ob
        die Variabel x gleich wie 15 ist. Falls es zutrifft, gib folgendes aus: Die Variabel x ist 15.

        Schreibe unter diesem Kommentar den Code.
         */
        int x = 15;
        if (x == 15){
            System.out.println("Die Variabel x ist 15.");
        }

        /*
        Nr. 2:
        Mit der bereits erstellten Variabel x kannst du jetzt eine Abfrage erstellen, welche überprüpft ob die Variabel x 10 ist, falls ja gib aus: Die Variabel x ist 10.
        Falls es nicht zutreffen sollte, gib in der Konsole aus: Die Variabel x ist nicht 10.
        */

        if (x == 10){
            System.out.println("Die Variabel x ist 10.");
        } else {
            System.out.println("Die Variabel x ist nicht 10.");
        }

    }

}
